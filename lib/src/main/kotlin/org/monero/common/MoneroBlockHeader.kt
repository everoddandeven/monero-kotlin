package org.monero.common

open class MoneroBlockHeader {
    val hash: String = ""
    val height: ULong = 0u
    val timestamp: ULong = 0u
    val size: ULong = 0u
    val weight: ULong = 0u
    val longTermWeight: ULong = 0u
    val depth: ULong = 0u
    val difficulty: ULong = 0u
    val cumulativeDifficulty: ULong = 0u
    val majorVersion: UInt = 0u
    val minorVersion: UInt = 0u
    val nonce: UInt = 0u
    val minerTxHash: String = ""
    val numTxs: UInt = 0u
    val orphanStatus: Boolean = false
    val prevHash: String = ""
    val reward: ULong = 0u
    val powHash: String = ""
}
